
// The 'switch' statement checks for eqaulity with '==='
// Use 'break' after each case
// or the cases after the correct one will be executed too.

var grade = 'B';
switch (grade) {
     case 'A':
          console.log('Great job!');
          break;
     case 'B':
          console.log('OK job');
          break;
     case 'C':
          console.log('You can do better');
          break;
     default:
          console.log('Oy vey');
          break;
}
