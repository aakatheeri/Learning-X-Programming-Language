# Strings
print('Hello World!')
print("This is also String.")

# Strings ca be added too
print("Hello "
       + "world!")
print("Hello " "world!")

# A string can be treated like a list of characters
print("Hello world!"[0])

# You can find the length of a string
print( len("This is a string") )

# f-strings
name = 'Ahmed'
print( f"{name} is {len(name)} characters long." )