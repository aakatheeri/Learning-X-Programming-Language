# The results of division is always a float
print(10.0 / 3)

# Modulo operation
print(7 % 3)
print(-7 % 3)

# Exponentiation (x**y, x to the yth power)
print(2**3)

# Boolean values are primitives
print(True)
print(False)

# negate with not
print(not True)
print(not False)

# Boolean operators
print( True and False)
print( False or True)

